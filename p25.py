import math

x = 5.7
y = 16
z = 5
floor_value = math.floor(x)
print(f"Floor of {x}: {floor_value}")

ceil_value = math.ceil(x)
print(f"Ceil of {x}: {ceil_value}")

factorial_value = math.factorial(z)
print(f"Factorial of {z}: {factorial_value}")

sqrt_value = math.sqrt(y)
print(f"Square root of {y}: {sqrt_value}")
